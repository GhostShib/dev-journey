# Crie um algoritimo que leie um numero e mostre seu dobro, triplo e raiz quadrada
# até a aula 7
print('Descubra o dobro, o triplo e a raiz quadrada de um numero!')
n = int(input('Digite um numero: '))
dobro = n * 2
triplo = n * 3
raiz = n ** 0.5
print('O dobro de {} é {}\nSeu triplo é {}\nE a raiz é {}'.format (n, dobro, triplo, raiz))